#include "libft.h"

void	free_array(char ***arr)
{
	int	i;

	i = 0;
	if (*arr != NULL)
	{
		while ((*arr)[i])
		{
			free((*arr)[i]);
			(*arr)[i] = NULL;
			i++;
		}
		free(*arr);
		*arr = NULL;
	}
}
